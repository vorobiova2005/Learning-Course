/*
  Орден потребує реєстрації. Заповни свою картку Джедая!
  Постав свої дані замість ???
  Пам'ятай: const для незмінних даних, let — для змінних.
*/

const heroName = "Valentyna";        // Твоє ім'я (рядок у лапках)
const homePlanet = "Naboo";      // Планета народження (рядок)
let heroAge = 20;             // Вік (число, без лапок)
let isJedi = true;              // Ти Джедай? (true або false)
let forcePower = 58;          // Сила Коду від 1 до 100 (число)

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 1.2 — ТИПИ ДАНИХ (typeof)
// ═══════════════════════════════════════════════════════════
/*
  Оператор typeof показує ТИП значення.
  Приклад: typeof "Привіт"  →  "string"
           typeof 42        →  "number"
           typeof true      →  "boolean"
           typeof null      →  "object"  ← це баг в JS, запам'ятай!
           typeof undefined →  "undefined"

  ЗАВДАННЯ: Дізнайся тип кожного значення
*/

const typeOfName    = typeof heroName;     // вже готово як приклад!
const typeOfAge     = typeof heroAge;                 // typeof heroAge
const typeOfIsJedi  = typeof isJedi;                 // typeof isJedi
const typeOfNull    = typeof null;                 // typeof null  ← яке буде значення?
const typeOfNothing = typeof undefined;                 // typeof undefined

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 1.3 — РЕСУРСИ ОРДЕНУ
// ═══════════════════════════════════════════════════════════
/*
  Темний Компілятор атакує! Треба порахувати наші ресурси.

  Математичні оператори:
  + (додавання)   - (віднімання)
  * (множення)    / (ділення)
  % (остача від ділення)   ** (степінь)

  Оператори присвоєння:
  += (додати і зберегти)   -= (відняти і зберегти)
  *= (помножити і зберегти)
*/

let crystals = 100;    // кристалів Сили
let soldiers = 50;     // солдатів
let ships = 12;        // кораблів

// Порахуй суму всіх ресурсів разом:
let totalResources = crystals + soldiers + ships;

// Скільки кристалів припадає на кожного солдата?
let crystalsPerSoldier = crystals / soldiers;

// У бою загинуло 8 солдатів. Оновити кількість (використай -=):
soldiers -= 8;

// Отримали підкріплення — ще 5 кораблів (використай +=):
ships += 5;

// Піднеси кількість кораблів до квадрату (ships ** 2):
let shipsSquared = ships ** 2;

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 1.4 — ГАЛАКТИЧНА РАДІОГРАМА
// ═══════════════════════════════════════════════════════════
/*
  Щоб відправити повідомлення, треба скласти рядок.

  Шаблонний рядок (template literal) — найкращий спосіб:
  `Привіт, ${ім'я}! Ти з ${планети}.`

  Конкатенація (старий спосіб):
  "Привіт, " + ім'я + "!"
*/

// Склади рядок використовуючи шаблонний рядок (backtick ` `):
// Результат: "Джедай [ім'я], планета [планета], вік [вік]"
const heroProfile = `Джедай ${heroName}, планета ${homePlanet}, вік ${heroAge}`;

// Скільки символів у імені героя? (використай .length)
const nameLength = heroName.length;

// Переведи ім'я героя у ВЕЛИКІ літери (використай .toUpperCase())
const heroNameUpper = heroName.toUpperCase();

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 1.5 — ПЕРШИЙ ЛЕЙТЕНАНТ
// ═══════════════════════════════════════════════════════════

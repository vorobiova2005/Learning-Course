/*
  Напиши функцію-оголошення (function declaration).
  Вона отримує ім'я і повертає рядок:
  "Хай буде з тобою Сила, [ім'я]!"
*/

function pryvitanniaPadavana(name) {
  return `Хай буде з тобою Сила, ${name}!`
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.2 — ПОТУЖНІСТЬ ЛАЙТСЕЙБЕРА
// ═══════════════════════════════════════════════════════════
/*
  Напиши функціональний вираз (const = function).
  Розраховує потужність лайтсейбера:
  power = crystalStrength * 10 + forceLevel * 5

  Повертає число.
*/

const potuzhnistLaitseyBera = function (crystalStrength, forceLevel) {
  const power = crystalStrength * 10 + forceLevel * 5
  return power
};

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.3 — СТРІЛОЧНА ФУНКЦІЯ: ПЕРЕВІРКА БОЙОВОГО СТАНУ
// ═══════════════════════════════════════════════════════════
/*
  Напиши СТРІЛОЧНУ функцію (arrow function).
  Отримує hp (здоров'я від 0 до 100) і повертає статус:
    hp > 70  → "Здоровий"
    hp > 30  → "Поранений"
    hp > 0   → "Критичний стан"
    hp <= 0  → "Знешкоджений"
*/

const statusBoitsia = (hp) => {
  if ( hp > 70) {
    return "Здоровий"
  } else if (hp > 30){
    return "Поранений"
  } else if (hp > 0){
    return "Критичний стан"
  } else {
    return "Знешкоджений"
  }
};

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.4 — ПАРАМЕТРИ ЗА ЗАМОВЧУВАННЯМ
// ═══════════════════════════════════════════════════════════
/*
  Луна готує корабель до вильоту.
  Функція otrymatyKorabl() має параметри зі значеннями за замовчуванням:
    - shipName    (за замовчуванням: "Millennium Falcon")
    - fuelLevel   (за замовчуванням: 100)
    - crew        (за замовчуванням: 2)

  Поверни рядок: "[назва]: паливо [%]%, команда [N] осіб"
  Приклад: "X-Wing: паливо 80%, команда 1 осіб"
*/

function otrymatyKorabl(shipName = "Millennium Falcon", fuelLevel = 100, crew = 2) {
  return `${shipName}: паливо ${fuelLevel}%, команда ${crew} осіб`;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.5 — REST ПАРАМЕТРИ: АТАКА ФЛОТУ
// ═══════════════════════════════════════════════════════════
/*
  Луна командує атакою. Кількість кораблів у залпі невідома.
  Функція повертає СУМУ сили всіх переданих кораблів.

  Приклад: atakaFlotu(100, 200, 50) → 350
           atakaFlotu(10) → 10

  Використай rest параметри: ...ships
*/

function atakaFlotu(...ships) {
  let total = 0
  for (let ship of ships){
    total = total + ship
  }
  return total
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.6 — ЗАМИКАННЯ: ЛІЧИЛЬНИК ПЕРЕМОГ
// ═══════════════════════════════════════════════════════════
/*
  Замикання (closure) — функція, що "пам'ятає" зовнішні змінні.

  Напиши функцію stvorytyCHchylnyk() яка:
  - зберігає внутрішній лічильник (починається з 0)
  - повертає функцію, яка:
    - збільшує лічильник на 1
    - повертає поточне значення

  Приклад:
    const peremohy = stvorytyCHchylnyk();
    peremohy(); → 1
    peremohy(); → 2
    peremohy(); → 3
*/

function stvorytyCHchylnyk() {
  let count = 0;
  return function () {
    count ++;
    return count
  };
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 4.7 — ФУНКЦІЯ ВИЩОГО ПОРЯДКУ (БОНУС ⭐)
// ═══════════════════════════════════════════════════════════
/*
  Функція вищого порядку — приймає або повертає функцію.

  Напиши zastosuvaty(fn, value):
  Вона приймає функцію fn і значення value,
  і повертає результат виклику fn(value).

  Приклад:
    const double = (x) => x * 2;
    zastosuvaty(double, 5) → 10
    zastosuvaty((x) => x + "!", "Луна") → "Луна!"
*/

function zastosuvaty(fn, value) {
  return fn(value)
}


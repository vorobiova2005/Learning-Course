// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.1 — ЛАЗЕРНИЙ ЗАЛП
// ═══════════════════════════════════════════════════════════
/*
  Луна стріляє з лазерної гармати count разів.
  Поверни масив з числами від 1 до count включно.

  Приклад: lazernyiZalp(5) → [1, 2, 3, 4, 5]

  Використай цикл for і метод масиву .push()
*/

function lazernyiZalp(count) {
  const shots = [];
  for (let i = 1; i <= count; i++) {
        shots.push(i)
  }

  return shots;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.2 — ЗВОРОТНІЙ ВІДЛІК ГІПЕРСТРИБКА
// ═══════════════════════════════════════════════════════════
/*
  Корабель готується до стрибка в гіперпростір.
  Поверни масив із зворотнім відліком від n до 1.

  Приклад: vidlik(5) → [5, 4, 3, 2, 1]

  Використай цикл for (починай від n, зменшуй)
*/

function vidlik(n) {
  const countdown = [];
  for (let i = n; i >= 1; i--) {
    countdown.push(i)
  }

  return countdown;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.3 — ГЕНЕРАТОР ЩИТА
// ═══════════════════════════════════════════════════════════
/*
  Генератор щита накопичує енергію поки не досягне maxEnergy.
  Кожна ітерація додає charge одиниць.

  Поверни скільки ітерацій знадобилось.

  Приклад: generatorShchyta(100, 15)
  → 0+15=15, 15+15=30, ..., до 100 → поверни кількість кроків

  Використай while
*/

function generatorShchyta(maxEnergy, charge) {
  let energy = 0;
  let steps = 0;

  while (energy < maxEnergy){
      energy += charge
      steps ++;
  }

  return steps;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.4 — СКАНЕР ФЛОТУ
// ═══════════════════════════════════════════════════════════
/*
  Просканируй флот кораблів і порахуй загальну потужність.
  ships — масив чисел (потужність кожного корабля).

  Використай for...of
  Приклад: skanerFlotu([100, 200, 150]) → 450
*/

function skanerFlotu(ships) {
  let totalPower = 0;

  for (const ship of ships) {
    totalPower += ship
  }

  return totalPower;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.5 — ПОШУК ЗРАДНИКА
// ═══════════════════════════════════════════════════════════
/*
  В масиві names знайди першого агента Темного Компілятора.
  Його позивний завжди починається з "DARK_"

  Поверни ім'я зрадника або null якщо не знайдено.

  Використай for...of і break
*/

function poshukZradnyka(names) {

  for (const dark of names) {
    if(dark.startsWith('DARK_')){
      return dark
    }
  }

  return null;  // якщо зрадника не знайдено
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 3.6 — ФІЛЬТР БАГ-ДРОЇДІВ
// ═══════════════════════════════════════════════════════════
/*
  Відфільтруй список: поверни лише тих, хто НЕ є Баг-Дроїдом.
  Баг-Дроїди мають isBug === true.

  Використай for...of і continue (пропускай Баг-Дроїдів)
  Або можна використати if всередині циклу.

  units — масив об'єктів: { name: "...", isBug: true/false }
  Поверни масив рядків (тільки імена НЕ-дроїдів)
*/

function filtrBahDroidiv(units) {
  const heroes = [];
  for (const droid of units) {
    if (droid.isBug === false){
      heroes.push(droid.name)
      continue;
    }

  }

  return heroes;
}


// 🎯 ЗАВДАННЯ 3.7 — ТАБЛИЦЯ МНОЖЕННЯ ДЖЕДАЯ (БОНУС ⭐)
// ═══════════════════════════════════════════════════════════
/*
  Створи таблицю множення для числа n (від 1 до 10).
  Поверни масив рядків у форматі: "n × 1 = n"

  Приклад: tablytsia(3) → ["3 × 1 = 3", "3 × 2 = 6", ..., "3 × 10 = 30"]
*/

function tablytsia(n) {
  const rows = [];
  for (let i = 1; i <= 10; i++) {
    let res = n * i
    rows.push(`${n} × ${i} = ${res}`)
  }

  return rows;
}


// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.1 — РЕЄСТРАЦІЯ ФЛОТУ
// ═══════════════════════════════════════════════════════════
/*
  Створи масив флоту з 4 кораблів: "X-Wing", "Y-Wing", "A-Wing", "B-Wing"
  Потім:
  1. Додай "Millennium Falcon" в кінець (push)
  2. Додай "Ghost" на початок (unshift)
  3. Видали перший корабель (shift) — він вийшов із ладу
  4. Видали останній корабель (pop) — він полетів на місію

  Поверни фінальний стан флоту.
*/

function reiestratsiiaFlotu() {
  let fleet = ["X-Wing", "Y-Wing", "A-Wing", "B-Wing"];

  fleet.push("Millennium Falcon")

  fleet.unshift("Ghost")

  fleet.shift()

  fleet.pop()

  return fleet;
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.2 — ПОШУК У БАЗІ ДАНИХ
// ═══════════════════════════════════════════════════════════
/*
  Знайди інформацію про корабель у флоті.

  chyIsniuieKorabl(fleet, name) → true/false (використай includes)
  znaidyPozytsiiu(fleet, name) → індекс або -1 (використай indexOf)
*/

function chyIsniuieKorabl(fleet, name) {
  return fleet.includes(name)
}

function znaidyPozytsiiu(fleet, name) {
  return fleet.indexOf(name)
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.3 — ЕСКАДРИЛЬЯ
// ═══════════════════════════════════════════════════════════
/*
  Отримай перші 3 кораблі для ескадрильї (без зміни оригіналу).
  Використай slice(початок, кінець).
*/

function pershaTroyakEskadrylia(fleet) {
  return fleet.slice(0, 3)
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.4 — З'ЄДНАННЯ ФЛОТІВ
// ═══════════════════════════════════════════════════════════
/*
  Об'єднай два флоти в один масив.
  Використай concat() або spread оператор [...a, ...b]
*/

function ziednaniaFlotiv(fleet1, fleet2) {
  const newFleet = [
      ...fleet1,
      ...fleet2
  ]
  return newFleet
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.5 — МАНІФЕСТ КОМАНДИ
// ═══════════════════════════════════════════════════════════
/*
  Перетвори масив імен у рядок через кому і пробіл.
  Приклад: ["Луна", "Хан", "Лея"] → "Луна, Хан, Лея"
  Використай join()
*/

function manifest(crew) {
  return crew.join(", ")
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.6 — БОЙОВЕ СОРТУВАННЯ
// ═══════════════════════════════════════════════════════════
/*
  Відсортуй кораблі за бойовою потужністю (від найменшої до найбільшої).
  powers — масив чисел.

  Використай sort() з функцією порівняння:
  arr.sort((a, b) => a - b)  ← від меншого до більшого
  arr.sort((a, b) => b - a)  ← від більшого до меншого

  ВАЖЛИВО: sort() змінює оригінальний масив!
*/

function boioveSortuvannia(powers) {
  return [...powers].sort((a, b) => a - b);
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.7 — ВИДАЛЕННЯ ПОШКОДЖЕНИХ КОРАБЛІВ
// ═══════════════════════════════════════════════════════════
/*
  Видали пошкоджений корабель за індексом із флоту.
  Поверни масив без нього (не змінюй оригінал).

  Підказка: використай slice() і spread, або filter() (вивчимо пізніше).
  Або slice(0, index) + slice(index + 1)
*/

function vydatyPoshkodzhenyi(fleet, index) {
  return [...fleet.slice(0, index), ...fleet.slice(index + 1)]
}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 5.8 — РОЗВІДКА (БОНУС ⭐)
// ═══════════════════════════════════════════════════════════
/*
  Дано масив кораблів з їх швидкістю: [{name, speed}, ...]
  Знайди НАЙШВИДШИЙ корабель і поверни його назву.

  Підказка: перебери масив і стеж за максимумом.
*/

function naishvydshyiKorabl(ships) {
  // ships = [{name: "X-Wing", speed: 100}, ...]
  let maxSpeed = -Infinity;
  let fastestShipName = "";

  for (const ship of ships) {
    if (ship.speed > maxSpeed) {
      maxSpeed = ship.speed;
      fastestShipName = ship.name;
    }
  }
  return fastestShipName
}


// Повертає "Активний" якщо isOnline === true, інакше "Офлайн"
function statusDroida(isOnline) {
  return isOnline === true ? "Активний" : "Офлайн";
}

// Повертає більше з двох чисел
function maxZDvokh(a, b) {
  return  a > b ? a : b;
}

// Повертає "Парне" або "Непарне" (підказка: число % 2 === 0)
function parneChyNi(num) {
  return num % 2 === 0 ? "Парне" : "Непарне";
}


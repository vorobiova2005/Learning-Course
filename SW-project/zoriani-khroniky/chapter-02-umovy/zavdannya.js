/*
  Визнач, чи є істота Джедаєм, Ситхом або нейтральною.
  Правило: якщо forceSide === "light" → "Джедай"
           якщо forceSide === "dark"  → "Ситх"
           інакше                    → "Нейтральний"
*/

function vyznachStoronuSyly(forceSide) {
  if (forceSide === "light") {
    return "Джедай"
  } else if (forceSide === "dark"){
    return "Ситх"
  } else {
    return "Нейтральний"
  }

}

// ═══════════════════════════════════════════════════════════
// 🎯 ЗАВДАННЯ 2.2 — КЛАСИФІКАТОР ПЛАНЕТ

/*
  У JavaScript деякі значення є "falsy" (хибні) навіть без false:
    false, 0, "", null, undefined, NaN

  Всі інші — "truthy" (правдиві).

  ЗАВДАННЯ: Перевір, чи є значення "truthy"
  Поверни true якщо значення truthy, false — якщо falsy
*/

function chyTruthy(value) {
  return !!value ;   // підказка: використай !! або Boolean()
}


/*
  У галактиці є різні типи планет. Визнач тип за назвою.
  Використай switch:

  "Татуїн"   → "Пустельна планета"
  "Хот"      → "Крижана планета"
  "Ендор"    → "Лісова планета"
  "Корусант" → "Планета-місто"
  інша       → "Невідома планета"
*/

function klasyfikatorPlanet(planetName) {
  switch (planetName) {
    case "Татуїн":
      return "Пустельна планета"
    case "Хот":
      return "Крижана планета"
    case "Ендор":
      return "Лісова планета"
    case "Корусант":
      return "Планета-місто"
    default:
      return "Невідома планета"
  }
}


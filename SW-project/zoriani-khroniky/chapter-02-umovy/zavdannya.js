/*
  Визнач рівень загрози ворога за його силою (enemyPower):
    >= 90 → "КРИТИЧНА ЗАГРОЗА — тікай!"
    >= 70 → "Висока загроза — обережно"
    >= 40 → "Середня загроза — будь готова"
    < 40  → "Мала загроза — можна атакувати"

  Використай if / else if / else
*/

function rivenyZahrozy(enemyPower) {
  if (enemyPower >= 90){
    return  "КРИТИЧНА ЗАГРОЗА — тікай!"
  } else if(enemyPower >= 70){
    return "Висока загроза — обережно"
  } else if (enemyPower >= 40){
    return "Середня загроза — будь готова"
  } else
    return "Мала загроза — можна атакувати"
}

